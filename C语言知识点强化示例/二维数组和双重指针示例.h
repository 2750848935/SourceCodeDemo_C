void 一维数组示例() {
	//C语言中，没有单独的字符串类型
	//使用字符串数组表示一个字符串
	char name[] = "你的名字";
	//ASCII码 a-97 A-65   \0 -> ASCII为0的字符，空字符
	//一个中文字符占 2 个字节
	//C语言中，字符串和字符串数组的区别：字符串就是结尾元素为\0的字符数组
	printf("字符串的长度：%d\n", strlen(name));
}


void 二维数组示例()
{
	char names[ROW][COL] = {
		"基尼太美", 
		"四川唐门", 
		"武当剑法", 
		"大理段氏", 
		"五岳剑派"
	};
	for (int i = 0; i < ROW; i++) {
		printf("%s\n", names[i]);
	}
}

